    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // On click of button Register
        
        String name,crs,email,pass;
        int roll;
        name = txtName.getText();
        roll = Integer.parseInt(txtRoll.getText());
        crs = txtCourse.getText();
        email = txtEmail.getText();
        pass = txtPass.getText();
        if(name.isEmpty() || crs.isEmpty() || email.isEmpty() || pass.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Enter All the Details");
        }
        else
        {
            try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/ILib","root","prasad123");
            Statement stm = con.createStatement();
            String qry = "SELECT * FROM tblUsers WHERE rno="+roll;
            ResultSet rs = stm.executeQuery(qry);
            if(rs.next())
            {
                JOptionPane.showMessageDialog(this, "User exists");
            }
            else
            {
                String sql = "INSERT INTO tblUsers (name, rno, course, password, email) VALUES ('"+name+"',"+roll+",'"+crs+"','"+pass+"','"+email+"')";
                stm.executeUpdate(sql);
                JOptionPane.showMessageDialog(this, "One user added to Database");
                txtName.setText(null);
                txtRoll.setText(null);
                txtCourse.setText(null);
                txtEmail.setText(null);
                txtPass.setText(null);
            
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
        }
        
    }//GEN-LAST:event_btnRegisterActionPerformed

